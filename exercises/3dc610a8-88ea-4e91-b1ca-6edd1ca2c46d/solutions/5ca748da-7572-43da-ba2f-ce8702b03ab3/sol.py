def sorting_hat(student_trait):
    if student_trait == 'bravery':
        return 'Gryffindor'
    elif student_trait == 'hardworking':
        return 'Hufflepuff'
    elif student_trait == 'intelligence':
        return 'Ravenclaw'
    elif student_trait == 'cunning':
        return 'Slytherin'
    else:
        return 'Unknown'